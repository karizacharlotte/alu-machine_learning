#!/usr/bin/env python3
"""
Module that contains a function to concatenate two arrays
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.
    Args:
        arr1: A list of integers or floats.
        arr2: A list of integers or floats.
    Returns:
        A new list containing the concatenated elements of arr1 and arr2.
    """
    return arr1 + arr2
